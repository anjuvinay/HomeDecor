const mongoose=require('mongoose')
const userSchema=mongoose.Schema({
    
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        unique:true,
        required:true
    },
    mobile:{
        type:Number,
        required:false
    },
    password:{
        type:String,
        required:false
    },
    is_admin:{
        type:Boolean,
        default:false
    },
    is_active:{
        type:Boolean,
        default:true
    },
    otp:{
        type:Number,
        default:null
    },
    address:[
        {
            fname:{
                type:String
            },
            lname:{
                type:String
            },
            country:{
                type:String
            },
            housename:{
                type:String,               
            },
            city:{
                type:String
            },
            state:{
                type:String
            },
            pincode:{
                type:Number
            },
            phone:{
                type:Number
            },
            email:{
                type:String
            },
            description:{
                type:String
            }

        }
    ],
    cart:[
        {
            productId:{
                type:mongoose.Schema.Types.ObjectId,
                ref:'Product'
            },
            quantity:{
                type:Number,
                default:1
            }
        }
    ],
    wallet:{
        type:Number,
        default:0
    },
    referralCode:{
        type:String
    }
})




module.exports=mongoose.model('User',userSchema) 